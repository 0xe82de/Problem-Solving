package boj.bronze;

import java.util.Scanner;

public class BOJ_2741_1 {

	public static void main(String[] args) {
		
		// io
		Scanner sc = new Scanner(System.in);
		
		final int N = sc.nextInt();
		
		for (int i = 1; i <= N; ++i) {
			System.out.println(i);
		}
		
		// io close
		sc.close();
	}

}
